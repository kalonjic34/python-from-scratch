even = [2, 4, 6, 8]
odd = [1, 3, 5, 7, 9]

# Prints the smallest and largest values in the even list
print(min(even))  # Output: 2
print(max(even))  # Output: 8

# Prints the smallest and largest values in the odd list
print(min(odd))  # Output: 1
print(max(odd))  # Output: 9

print() 

# Prints the length (number of elements) of the even and odd lists
print(len(even))  # Output: 4
print(len(odd))  # Output: 5

print() 

